package com.training.sapTraining.model;

public class Scholar {
	
	private String scholarId;
	private String scholarName;
	private String scholarBatch;
	
	public String getScholarId() {
		return scholarId;
	}
	public void setScholarId(String scholarId) {
		this.scholarId = scholarId;
	}
	public String getScholarName() {
		return scholarName;
	}
	public void setScholarName(String scholarName) {
		this.scholarName = scholarName;
	}
	public String getScholarBatch() {
		return scholarBatch;
	}
	public void setScholarBatch(String scholarBatch) {
		this.scholarBatch = scholarBatch;
	}
	
}
